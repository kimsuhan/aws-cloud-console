function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_match, key) => String(params[key] ?? ''))
}

function resolveMessage(language: AppLanguage, key: string, params?: Record<string, string | number>): string {
  const value = messages[language][key] ?? messages.en[key] ?? key
  if (typeof value === 'function') {
    return value(params)
  }

  return interpolate(value, params)
}

interface I18nContextValue {
  language: AppLanguage
  t: (key: string, params?: Record<string, string | number>) => string
}

const I18nContext = createContext<I18nContextValue>({
  language: 'en',
  t: (key, params) => resolveMessage('en', key, params)
})

export function detectAppLanguage(locale: string | undefined): AppLanguage {
  return locale?.toLowerCase().startsWith('ko') ? 'ko' : 'en'
}

export function translate(language: AppLanguage, key: string, params?: Record<string, string | number>): string {
  return resolveMessage(language, key, params)
}

export function I18nProvider({
  language,
  children
}: {
  language: AppLanguage
  children: React.ReactNode
}): React.JSX.Element {
  return (
    <I18nContext.Provider
      value={{
        language,
        t: (key, params) => resolveMessage(language, key, params)
      }}
    >
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext)
}
